        <h3> Manualul utilizatorului - creat pentru utilizatorii obisnuiti, detaliaza procedeul de utilizare al site-ului, ce poate un user obtine prin folosirea acestuia si cum sa valorifice la maxim functionalitatile implementate. </h3>
        <h2> Home </h2>
        <p> Pagina principala, are rolul de a oferi informatii exacte despre calamitatile petrecute prin imagini sugestive proiectate pe o harta globala, precum si detalii amanuntite despre cea mai recenta calamitate, prezentate sub forma unui tabel, sub harta.
            Harta contine layerele aplicate de Google hartii oficiale Google Crisis Map si layerele adaugate de adminstrator pentru calamitatile locale.
            De asemenea, functia de "Send alerts!" se activeaza pe aceasta pagina si afiseaza un "alert" cu scopul de a incuraja userul sa caute adapost, cu cateva detalii despre calamitatea ce il pune in pericol, gravitatea acesteia si disponibilitatea unor locuri sigure in apropiere.</p>
        <h2> Find a Person </h2>
        <p> Ofera user-ului posibilitatea de a cauta o persoana gasita de autoritati sau declarata disparuta, precum si de a completa un formular pentru a introduce in baza de date o persoana gasita de el sau o persoana sesizata de user ca fiind disparuta.
        Apasarea butonului de "Missing Persons" sau "Found persons" va declansa aparitia unei bare de cautare si va returna fie un mesaj, in caz de esec, fie datele despre perrsoana cautata in caz de reusita. Butonul "I have found someone" va afisa un formular pe care userul trebuie sa-l completeze,
        urmand ca formularul sa fie trimis administratorului pentru verificare si apoi introdus in baza de date.

        <h2> Get to Safety </h2>
        <p> Ofera utilizatorului optiunea de a alege modul de deplasare, dupa care ii ofera acestuia un traseu catre cel mai apropiat safehouse de pozitia asta. Dupa alegerea modului de deplasare, se va arata traseul corespunzator modului de deplasare pe harta si sub aceasta se vor afisa indicatii pentru ajungerea la locul respectiv. </p>
        <h2> Contact Page </h2>
        <p> Aici gasim informatii legate de datele de contact ale administratorilor. </p>
        <h2> Login </h2>
        <p> Pe pagina de Login, utilizatorul va avea de ales intre a accesa pagina principala din perspectiva de admin, dupa ce s-a logat, sau din perspectiva de Guest, unde nu va avea access la comenzile adminilor. Tot pe pagina de Login, se gaseste si butonul de Forgot Password, care va trimite utilizatorul la un formular, care odata completat cu date corecte, va returna parola corespunzatoare.</p>
        <h2> Register </h2>
        <p> Butonul de Sign Up va trimite utilizatorul la un formular, care odata completat, va introduce userul in baza de date a User-ilor </p>

        <br> <br> <br>


        <h3> Manualul utilizatorului - acesta este ghidul de utilizare a aplicatiei de catre autoritati Cric care vor contribui la intretinerea aplicatiei si la actiunile de salvare a
            populatiei afectata de dezastre naturale. </h3>

        <h2>Administrator</h2>
        <p>Administratorul are abilitatea de a modifica informatiile puse pentru guests.Folosind butonul "Addauga Persoana disparuta" administratorul poate adauga in mod oficial persoanele disparute sau gasite.</p>

        <p>Pagina mai contine si lista cu persoanele date disparute de catre utilizatori pe care administratorul o poate verifica pentru a afisa pentru utilizatori persoanele disparute oficial.</p>
        <p>Butonul "Logout" din josul paginii are rolul de a deconecta administratorul de pe contul sau.</p>
        <h2> Home Page </h2>
        <p> In aceasta pagina se gasesc informatii despre celelalte sectiuni ale aplicatiei, impreuna cu o suita de statistici privitoare la activitatea depusa de asociatia Cric din momentul
            infiintarii. De asemenea, sunt oferite cateva imagini de la cele mai recente misiuni de salvare. </p>
        <h2> Manager Calamitate </h2>
        <p>Prin utilizarea acestui buton administratorul va fi redirectionat catre un formular unde poate introduce coordonatele pentru o calamitate ce va aparea pe harta de pe website.</p>
        <h2> Manager Safety </h2>
        <p>Prin utilizarea butonului "Manager Safety" administratorul vai fi redirectionat catre un formular unde poate introduce coordonatele pentru un safety zone care va aparea pe harta de pe website si va oferi guest-ului ruta cea mai scurta catre safezone.</p>
        <h2> Send Alert </h2>
        <p>Butonul "Send Alert" ofera un formular care dupa completare va construi in spate o alerta care va fi afisata pe mainpage in legatura cu calamitatea din zona guest-ului.</p>
        <h2>Main Page</h2>
        <p>Butonul "Main page" va directiona administratorul catre pagina principala pe care o poate accesa si guest-ului.</p>


